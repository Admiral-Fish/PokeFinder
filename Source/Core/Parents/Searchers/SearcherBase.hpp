#ifndef SEARCHERBASE_HPP
#define SEARCHERBASE_HPP

#include <Core/Global.hpp>
#include <atomic>
#include <mutex>
#include <vector>

/**
 * @brief Parent searcher class that stores concurrency attributes
 *
 * @tparam Result Result class that is generated by the searcher
 */
template <class Result>
class SearcherBase
{
public:
    /**
     * @brief Construct a new SearcherBase object
     *
     * @param method Encounter method
     * @param profile Profile Information
     * @param filter State filter
     */
    SearcherBase() : progress(0), searching(false)
    {
    }

    /**
     * @brief Cancels the running search
     */
    void cancelSearch()
    {
        searching = false;
    }

    /**
     * @brief Returns the progress of the running search
     *
     * @return Progress
     */
    virtual int getProgress() const
    {
        return progress;
    }

    /**
     * @brief Returns the states of the running search
     *
     * @return Vector of computed states
     */
    std::vector<Result> getResults()
    {
        std::lock_guard<std::mutex> guard(mutex);
        auto data = std::move(results);
        return data;
    }

protected:
    std::mutex mutex;
    std::vector<Result> results;
    std::atomic<u32> progress;
    bool searching;
};

#endif // SEARCHERBASE_HPP
